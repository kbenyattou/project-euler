numberint = int(numberstring.replace('\n', ''))

# This function takes a string of integers and converts it into a list of its
# constituent integers e.g. '1234' converts to [1,2,3,4].

def convertlist(x):
    x = list(x)
    for j in range(0,len(x)):
        x[j] = int(x[j])
    return x

# This function will take a string of integers and find the product of its
# consitutent digits.

def prod(x):
    x = convertlist(x)
    product = 1
    for i in x:
        product = product * i
    return product

# We'll create a set of all the possible adjacent 13-digit strings from the
# given 1000-digit number.

digitstrings = []
for i in range(0,1001-13):
    digitstrings.append(str(numberint)[i:i+13])

# Given a list of integer strings, prodmax() returns the string with the
# greatest product of its constituent digits.

def prodmax(x):
    term = x[0]
    i = 0
    while i < len(x):
        if prod(term) < prod(x[i]):
            term = x[i]
        i += 1
    return convertlist(term)

